/**
 * Check if a string is a common region rather than a country
 * @param name - The name to check
 * @returns Boolean indicating if the string is a common region
 */
export function isCommonRegion(name: string): boolean {
  return commonRegions.some(
    region => region.toLowerCase() === name.toLowerCase()
  );
}

/**
 * Get all valid countries
 * @returns Array of all valid countries
 */
export function getAllCountries(): string[] {
  return validCountries;
}
