/**
 * Check if a string is a valid country name
 * @param country - The country name to check
 * @returns Boolean indicating if the string is a valid country name
 */
export function isValidCountry(country: string): boolean {
  return validCountries.some(
    validCountry => validCountry.toLowerCase() === country.toLowerCase()
  );
}

