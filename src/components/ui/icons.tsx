// Icon component with consistent styling
interface IconProps {
  name: keyof typeof icons;
  size?: number;
  className?: string;
  color?: string;
}

export function Icon({ name, size = 24, className = '', color = 'currentColor' }: IconProps) {
  const icon = icons[name];

  if (!icon) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }

  return (
    <span
      className={`inline-flex items-center justify-center ${className}`}
      style={{
        width: size,
        height: size,
        color,
      }}
    >
      {icon}
    </span>
  );
}

// Social icon component with specific styling
interface SocialIconProps extends IconProps {
  platform: 'facebook' | 'twitter' | 'linkedin' | 'instagram' | 'whatsapp';
  url: string;
}

export function SocialIcon({ platform, url, size = 24, className = '' }: SocialIconProps) {
  const handleClick = () => {
    window.open(url, '_blank', 'noopener,noreferrer');
  };

  return (
    <button
      onClick={handleClick}
      className={`inline-flex items-center justify-center rounded-full transition-all duration-200 hover:scale-110 ${className}`}
      style={{
        width: size,
        height: size,
      }}
      aria-label={`Share on ${platform}`}
    >
      <Icon name={platform} size={size} />
    </button>
  );
}

